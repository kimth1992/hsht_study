package hyungmin_study;

import java.util.Scanner;

public class No_8393 {

  public static void main(String[] args) {
    Scanner keyboardScan = new Scanner(System.in);
    int n = keyboardScan.nextInt();
    int sum = 0;
    for (int i=1; i<=n; i++) {
      sum = sum+i;
    }
    System.out.println(sum);





  }
}
